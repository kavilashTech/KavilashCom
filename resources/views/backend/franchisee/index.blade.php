@section('script')
    <script type="text/javascript">
        
        $(document).on("change", ".check-all", function() {
            if(this.checked) {
                // Iterate each checkbox
                $('.check-one:checkbox').each(function() {
                    this.checked = true;                        
                });
            } else {
                $('.check-one:checkbox').each(function() {
                    this.checked = false;                       
                });
            }
          
        });
        
        function sort_customers(el){
            $('#sort_customers').submit();
        }
        function confirm_ban(url)
        {
            $('#confirm-ban').modal('show', {backdrop: 'static'});
            document.getElementById('confirmation').setAttribute('href' , url);
        }

        function confirm_unban(url)
        {
            $('#confirm-unban').modal('show', {backdrop: 'static'});
            document.getElementById('confirmationunban').setAttribute('href' , url);
        }
        
        function bulk_delete() {
            var data = new FormData($('#sort_customers')[0]);
            $.ajax({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                url: "{{route('bulk-customer-delete')}}",
                type: 'POST',
                data: data,
                cache: false,
                contentType: false,
                processData: false,
                success: function (response) {
                    if(response == 1) {
                        location.reload();
                    }
                }
            });
        }

        $(document).on('click','.approved',function(){
            var userid =  $(this).data('id');
            $.ajax({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                url: "{{route('franchisee.approved')}}",
                type: 'post',
                data: {
                    userid  : userid
                },
                dataType: 'json',
                
                success: function (response) {
                    if(response == 1) {
                        alert("franchisee Approved");
                        location.reload();
                    }
                }
            });

            
        });
        $(document).on('click','.rejected',function(){
            $('.franchisee_id').val('');
            $('#rejected_reason').text('');

             $('.franchisee_id').val($(this).data('id'));


        })


        $(document).on('click','.rejaected_button',function(){
            var userid =  $('.franchisee_id').val();
            var reason = $('#rejected_reason').val();
            $.ajax({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                url: "{{route('franchisee.rejected')}}",
                type: 'post',
                data: {
                    userid  : userid,
                    reason   : reason,
                },
                dataType: 'json',
                
                success: function (response) {
                    if(response == 1) {
                        alert("franchisee Rejected");
                        location.reload();
                    }
                }
            });

            
        });


    </script>
@endsection
